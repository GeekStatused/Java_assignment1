
public class Assignment_01 {

	public static void main(String[] args) {
		
		//Variable 1 initialized to 3
		int num1 = 3;
		
		//Variable 2 initialized to 23
		float num2 = 23;
		
		//Variable for the sum of both. Type casted to hold an int since we are narrowing.
		int sum = (int) (num1 + num2); 
		
		
		//Print out the value of sum to the console
		System.out.println(sum);

	}

}
